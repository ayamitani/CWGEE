#' Description of perio.rda
#'
#' Longitudinal Periodontal Data Set
#'
#' A simulated data set closely resembling the VA Dental Longitudinal Study containing longitudinal observation of
#' 459 participants with clinical attachment loss and clinical/demographic information.
#'
#' @format A data frame with 54124 rows and 8 variables:
#' \describe{
#'   \item{subject}{Subject ID}
#'   \item{tooth}{Tooth ID}
#'   \item{visit}{Visit number}
#'   \item{smoking}{Baseline smoking status}
#'   \item{edu}{Level of education, 0: No college, 1: College degree or higher}
#'   \item{mets}{Baseline metabolic syndrome status}
#'   \item{cal}{Clinical attachment loss, 1: <2mm, 2: 2-2.9mm, 3: 3-4.9mm, 4: >=5mm}
#'   \item{age}{Baseline age}
#'   ...
#' }
#' For more information, refer to manuscript:
#' Mitani AA, Kaye EK, Nelson KP. Marginal analysis of ordinal clustered longitudinal data with informative
#' cluster size.

